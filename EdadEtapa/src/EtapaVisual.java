    private void BtnVerificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnVerificarMouseClicked
        
        int Edad;
        
        Edad = Integer.parseInt(TxtEdad.getText());
        
        if (Edad <0){
        JOptionPane.showMessageDialog(null, "Fuera de los limites");
        }else if (Edad >=1 && Edad <6){
            TxtEtapa.setText("Eres un bebe");
        }else if (Edad >=6 && Edad <=12){
            TxtEtapa.setText("Eres un niÃ±o");
        }else if (Edad >=13 && Edad <=15){
            TxtEtapa.setText("Eres un adolescente");
        }else if (Edad >=16 && Edad <=18){
            TxtEtapa.setText("Eres un joven");
        }else if (Edad >=19 && Edad <=30){
            TxtEtapa.setText("Eres un Adulto joven");
        }else if (Edad >=31 && Edad <=59){
            TxtEtapa.setText("Eres un Adulto");
        }else if (Edad >= 60 && Edad <=110){
            TxtEtapa.setText("Eres un adulto mayor");
        }else if (Edad >110){
            JOptionPane.showMessageDialog(null, "Fuera de los limites");
        }
            
    }//GEN-LAST:event_BtnVerificarMouseClicked

